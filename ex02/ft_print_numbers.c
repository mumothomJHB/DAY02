void ft_putchar(char c)

{
	write(1, &c, 1);
}

void ft_print_numbers(void)
{
	char c;
	char m;

	c = '1';
	m = '9';

	while ( c <= m )
	{
		ft_putchar(c);
		c++;
	}
	
}
